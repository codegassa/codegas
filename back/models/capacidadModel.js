let mongoose = require('mongoose')
let Schema   = mongoose.Schema;
 
//////////////////////////////////////////////////////////////////////////////
////////***********     creo el esquema / ciudad        ****//////////////
//////////////////////////////////////////////////////////////////////////////
let Capacidad = new Schema({
    creado 	   : String,
    capacidad  : String,
    eliminado  : {type:Boolean, default:false},  
})

module.exports = mongoose.model('Capacidad', Capacidad) 